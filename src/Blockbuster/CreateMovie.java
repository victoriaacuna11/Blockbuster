    public boolean validateMovieName(){
        if(!this.movieName.getText().isEmpty() && this.movieName.getText().length()<31){
            return true;
        } else {
            return false;
        }
    }
    
    public boolean validateID(){
        if(!this.movieID.getText().isEmpty() && this.movieID.getText().length()<7){
            if(Metodos.isNumeric(this.movieID.getText())){
                if((Integer.parseInt(this.movieID.getText()))<1000000 && (Integer.parseInt(this.movieID.getText()))>0){
                    return true;
                }else {
                    return false;
                }
            } else {
                return false;
            }
        }else{
            return false;
        }
    }
    
    
    public MovieIndex[] ordenarIndices(MovieIndex[] newMIs){
        for (int i = 0; i < newMIs.length - 1; i++){
            int min = i;
            for (int j = i + 1; j < newMIs.length; j++){
                if (newMIs[j].getMovieID()< newMIs[min].getMovieID()){
                    min = j;
                }
            }
            if (i != min){
                MovieIndex aux= newMIs[i];
                newMIs[i] = newMIs[min];
                newMIs[min] = aux;
            }
        }
        return newMIs;
    }
    

