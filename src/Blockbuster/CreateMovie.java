    private void CreateMovieActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CreateMovieActionPerformed
        
        if(this.validateMovieName()){
            if(this.validateID()){
                        if(!Cartelera.firstMovie){
                            Pelicula m = new Pelicula(this.movieName.getText(), this.movieGenre.getSelectedItem().toString(), Integer.parseInt(this.movieID.getText()));
                            this.newMovies = new Pelicula[1];
                            this.newMovies[0] = m;
                            Cartelera.firstMovie=true;
                            MovieIndex[] mi = new MovieIndex[1];
                            MovieIndex index = new MovieIndex(m.getID(),0);
                            mi[0] = index;
                            Cartelera.movieIndexes = mi;
                            Cartelera.movies=this.newMovies;
                            this.setVisible(false); 
                        } else {
                            int indiceTemp = Cartelera.buscarPelicula(Integer.parseInt(this.movieID.getText()));
                            if(indiceTemp==-1){
                                Pelicula m = new Pelicula(this.movieName.getText(), this.movieGenre.getSelectedItem().toString(), Integer.parseInt(this.movieID.getText()));
                                this.newMovies = new Pelicula[this.movies.length + 1];
                                MovieIndex[] mi = new MovieIndex[this.newMovies.length];
                                MovieIndex index = new MovieIndex(m.getID(),mi.length-1);

                                for (int i = 0; i < this.movies.length; i++) {
                                    this.newMovies[i]=this.movies[i];
                                    mi[i]=Cartelera.movieIndexes[i];
                                }
                                this.newMovies[this.newMovies.length-1]=m;
                                mi[mi.length-1]=index;
                                mi=this.ordenarIndices(mi);
                                Cartelera.movieIndexes=mi; 
                                Cartelera.movies=this.newMovies;
                                this.setVisible(false); 
                            } else {
                                JOptionPane.showMessageDialog(this, "El ID ya pertenece a la película titulada \""+Cartelera.movies[indiceTemp].getName()+"\"", "ERROR", JOptionPane.ERROR_MESSAGE);
                                this.movieID.setText("");
                            }
                        }
            } else {
                JOptionPane.showMessageDialog(this, "El ID es inválido.\nDebe ser un número entero entre el 1 y el 999999", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "El nombre de la película debe ser máximo de 30 caracteres.", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
          
    }//GEN-LAST:event_CreateMovieActionPerformed

